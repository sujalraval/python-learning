# Initialize a variable
x = 10
print("Initial value of x:", x)  # Output: 10

# 1. Shorthand Addition
x += 5  # Equivalent to x = x + 5
print("After shorthand addition (x += 5):", x)  # Output: 15

# 2. Shorthand Subtraction
x -= 3  # Equivalent to x = x - 3
print("After shorthand subtraction (x -= 3):", x)  # Output: 12

# 3. Shorthand Multiplication
x *= 2  # Equivalent to x = x * 2
print("After shorthand multiplication (x *= 2):", x)  # Output: 24

# 4. Shorthand Division
x /= 4  # Equivalent to x = x / 4
print("After shorthand division (x /= 4):", x)  # Output: 6.0

# 5. Shorthand Floor Division
x //= 2  # Equivalent to x = x // 2
print("After shorthand floor division (x //= 2):", x)  # Output: 3.0

# 6. Shorthand Modulo
x %= 2  # Equivalent to x = x % 2
print("After shorthand modulo (x %= 2):", x)  # Output: 1.0

# 7. Shorthand Exponentiation
x **= 3  # Equivalent to x = x ** 3
print("After shorthand exponentiation (x **= 3):", x)  # Output: 1.0

# Conclusion:
# Shorthand operations provide a compact and readable way
# to perform arithmetic operations and update variable values.
