# 1. Addition
a = 10
b = 5
addition_result = a + b
print("Addition:", a, "+", b, "=", addition_result)  # Output: 15

# 2. Subtraction
subtraction_result = a - b
print("Subtraction:", a, "-", b, "=", subtraction_result)  # Output: 5

# 3. Multiplication
multiplication_result = a * b
print("Multiplication:", a, "*", b, "=", multiplication_result)  # Output: 50

# 4. Division
division_result = a / b
print("Division:", a, "/", b, "=", division_result)  # Output: 2.0

# 5. Floor Division
floor_division_result = a // b
print("Floor Division:", a, "//", b, "=", floor_division_result)  # Output: 2

# 6. Modulo (Remainder)
modulo_result = a % b
print("Modulo:", a, "%", b, "=", modulo_result)  # Output: 0

# 7. Exponentiation
exponentiation_result = a ** b
print("Exponentiation:", a, "**", b, "=", exponentiation_result)  # Output: 100000

# 8. Combining Operations
combined_result = (a + b) * (a - b)
print("Combined Operation (a + b) * (a - b):", combined_result)  # Output: 75

# Conclusion:
# Python supports a wide range of arithmetic operations,
# making it easy to perform calculations and manipulate numerical data.
